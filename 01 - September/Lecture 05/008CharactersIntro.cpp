#include<iostream>

using namespace std;

int main() {

	char ch; 

	ch = 'A';

	cout << ch << endl;

	cout << (int)ch << endl;

	ch = 'B';

	cout << ch << endl;

	cout << (int)ch << endl;

	ch = 'z';

	cout << ch << endl;

	cout << (int)ch << endl;

	ch = '7';

	cout << ch << endl;

	cout << (int)ch << endl;

	ch = 90;

	cout << ch << endl;

	return 0;
}